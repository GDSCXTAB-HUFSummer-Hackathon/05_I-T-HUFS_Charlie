import UIKit

extension UIFont {
    class func D2Coding(size: CGFloat) -> UIFont {
        return UIFont(name: "D2Coding", size: size)!
    }

    class func D2CodingBold(size: CGFloat) -> UIFont {
        return UIFont(name: "D2CodingBold", size: size)!
    }
}

